void render_init(void)
{
    DISPLAY_OFF;
    SPRITES_8x16;
    set_bkg_data(0u, TILE_COUNT, terrain_tiles);
    set_sprite_data(TILE_COUNT, PLAYER_TILE_COUNT, player_tiles);
    set_sprite_tile(0u, TILE_COUNT);
    render_world();
    SHOW_BKG;
    SHOW_SPRITES;
    DISPLAY_ON;
}

void render_world(void)
{
    set_bkg_tiles(0u, 0u, WORLD_WIDTH_TILES, WORLD_HEIGHT_TILES, world_tiles());
}

void render_frame(const Camera *camera, const Player *player)
{
    int16_t sprite_x = (int16_t)(player->x - camera->x + 8);
    int16_t sprite_y = (int16_t)(player->y - camera->y + 16);

    scroll_bkg((int16_t)camera->x - SCX_REG, (int16_t)camera->y - SCY_REG);
    move_sprite(0u, (uint8_t)sprite_x, (uint8_t)sprite_y);
}
